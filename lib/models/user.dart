class User {
  final String id;
  final String nom;
  final String prenom;
  final String email;
  final String motDePasse;
  final String telephone;
  final String adresse;

  User({
    required this.id,
    required this.nom,
    required this.prenom,
    required this.email,
    required this.motDePasse,
    required this.telephone,
    required this.adresse,
  });
}
